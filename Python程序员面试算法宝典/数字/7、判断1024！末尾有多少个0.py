# 方法2：因子法
"""
5 与偶数相乘会增加0

是5的倍数的有            1024//5      = 204
是25的倍数的有           1024//25     = 40
是125的倍数的有          1024//125    = 8
是625的倍数的有          1024//625    = 1
"""


def zeroCount(n):
    count = 0
    while n > 0:
        n = n // 5
        count += n
    return count


if __name__ == '__main__':
    print('1024！末尾的0的个数为：', zeroCount(1024))
